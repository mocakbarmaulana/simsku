    public function index(Request $request)
    {
        $active = 'Payment';
        $q = $request->q;

        $payments = Payment::where('name_transfer', 'like', "%{$q}%")
                            ->orderBy('created_at', 'DESC')
                            ->paginate(10);

        return view('admin.payment.index', compact('active', 'payments'));
    }

